//! Deterministic, invertible floor-symbol operator catalog for the Omega codec.
//!
//! This module deliberately separates two provenance classes:
//!
//! * operators 0..=7 preserve the inherited C2^3 R/N/Q algebra, reproducing
//!   its 8-bit views exactly from `pais_omega_floor1.rs`; 6/10/12-bit symbols
//!   use the specified same-width half-swap/per-half-bit-reverse extension;
//! * operators 8..=39 are newly specified computational byte permutations.
//!
//! The sector and radial/lens names describe software coordinate transforms.
//! They make no claim about physical sectors, lenses, cosmology, or quantum
//! behavior.

