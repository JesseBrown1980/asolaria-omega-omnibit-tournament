    fn deterministic_payload(length: usize, salt: u64) -> Vec<u8> {
        let mut state = 0x9e37_79b9_7f4a_7c15_u64 ^ salt ^ length as u64;
        (0..length)
            .map(|index| {
                state ^= state << 13;
                state ^= state >> 7;
                state ^= state << 17;
                (state as u8)
                    .wrapping_add(index as u8)
                    .rotate_left((index % 8) as u32)
            })
            .collect()
    }

    fn deterministic_symbols(length: usize, bits: u8, salt: u64) -> Vec<u16> {
        let mask = (1_u16 << bits) - 1;
        let mut state = 0xd1b5_4a32_d192_ed03_u64 ^ salt ^ length as u64 ^ bits as u64;
        (0..length)
            .map(|index| {
                state ^= state << 13;
                state ^= state >> 7;
                state ^= state << 17;
                ((state as u16).wrapping_add(index as u16)) & mask
            })
            .collect()
    }

