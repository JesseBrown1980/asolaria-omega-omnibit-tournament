#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct UnknownOperator(pub u8);

impl fmt::Display for UnknownOperator {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "unknown Omega operator id {}", self.0)
    }
}

impl Error for UnknownOperator {}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum SymbolTransformError {
    UnknownOperator(u8),
    InvalidFloorBits(u8),
    SymbolOutOfRange { index: usize, value: u16, bits: u8 },
}

impl fmt::Display for SymbolTransformError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match *self {
            Self::UnknownOperator(id) => write!(f, "unknown Omega operator id {id}"),
            Self::InvalidFloorBits(bits) => {
                write!(f, "invalid floor width {bits}; expected one of 6,8,10,12")
            }
            Self::SymbolOutOfRange { index, value, bits } => write!(
                f,
                "symbol {value} at index {index} is outside the {bits}-bit floor"
            ),
        }
    }
}

impl Error for SymbolTransformError {}

pub fn operator_spec(id: u8) -> Result<&'static OperatorSpec, UnknownOperator> {
    OPERATORS.get(id as usize).ok_or(UnknownOperator(id))
}

fn validate_symbols(bits: u8, input: &[u16]) -> Result<(), SymbolTransformError> {
    if !matches!(bits, 6 | 8 | 10 | 12) {
        return Err(SymbolTransformError::InvalidFloorBits(bits));
    }
    let limit = 1_u16 << bits;
    for (index, value) in input.iter().copied().enumerate() {
        if value >= limit {
            return Err(SymbolTransformError::SymbolOutOfRange { index, value, bits });
        }
    }
    Ok(())
}

/// Floor-native API. Values are symbols, not packed bytes.
pub fn apply_symbols(
    id: u8,
    bits: u8,
    input: &[u16],
) -> Result<Vec<u16>, SymbolTransformError> {
    operator_spec(id).map_err(|_| SymbolTransformError::UnknownOperator(id))?;
    validate_symbols(bits, input)?;
    Ok(match id {
        0..=7 => apply_rnq_symbols(id, bits, input),
        8..=19 => apply_sector_symbols(id, input),
        20..=39 => apply_lens_symbols(id, input),
        _ => unreachable!("operator_spec rejected the id"),
    })
}

/// Floor-native inverse. PI20 and TRI12 use explicit computed scatters.
pub fn invert_symbols(
    id: u8,
    bits: u8,
    input: &[u16],
) -> Result<Vec<u16>, SymbolTransformError> {
    let spec = operator_spec(id).map_err(|_| SymbolTransformError::UnknownOperator(id))?;
    validate_symbols(bits, input)?;
    Ok(match id {
        8..=19 => invert_sector_symbols(id, input),
        20..=39 => invert_lens_symbols(id, input),
        _ => apply_rnq_symbols(spec.inverse_id, bits, input),
    })
}

/// 8-bit compatibility wrapper over the floor-native symbol API.
pub fn apply_operator(id: u8, input: &[u8]) -> Result<Vec<u8>, UnknownOperator> {
    operator_spec(id)?;
    let symbols = input.iter().map(|value| *value as u16).collect::<Vec<_>>();
    let output = apply_symbols(id, 8, &symbols).expect("u8 values are valid 8-bit symbols");
    Ok(output.into_iter().map(|value| value as u8).collect())
}

/// 8-bit compatibility inverse over the floor-native symbol API.
pub fn invert_operator(id: u8, input: &[u8]) -> Result<Vec<u8>, UnknownOperator> {
    operator_spec(id)?;
    let symbols = input.iter().map(|value| *value as u16).collect::<Vec<_>>();
    let output = invert_symbols(id, 8, &symbols).expect("u8 values are valid 8-bit symbols");
    Ok(output.into_iter().map(|value| value as u8).collect())
}

// ---- Inherited exact C2^3 R/N/Q transforms -------------------------------

fn t_r_symbols(input: &[u16]) -> Vec<u16> {
    input.iter().rev().copied().collect()
}

fn t_n_symbols(bits: u8, input: &[u16]) -> Vec<u16> {
    let half = bits / 2;
    let half_mask = (1_u16 << half) - 1;
    input
        .iter()
        .map(|symbol| ((symbol & half_mask) << half) | ((symbol >> half) & half_mask))
        .collect()
}

fn reverse_low_bits(mut value: u16, width: u8) -> u16 {
    let mut reversed = 0_u16;
    for _ in 0..width {
        reversed = (reversed << 1) | (value & 1);
        value >>= 1;
    }
    reversed
}

fn t_q_symbols(bits: u8, input: &[u16]) -> Vec<u16> {
    let half = bits / 2;
    let half_mask = (1_u16 << half) - 1;
    input
        .iter()
        .map(|symbol| {
            (reverse_low_bits(symbol >> half, half) << half)
                | reverse_low_bits(symbol & half_mask, half)
        })
        .collect()
}

/// Floor-native extension of exact `geometry_view`: R bit 0, N bit 1, Q bit 2.
fn apply_rnq_symbols(mask: u8, bits: u8, input: &[u16]) -> Vec<u16> {
    let mut output = input.to_vec();
    if mask & 1 != 0 {
        output = t_r_symbols(&output);
    }
    if mask & 2 != 0 {
        output = t_n_symbols(bits, &output);
    }
    if mask & 4 != 0 {
        output = t_q_symbols(bits, &output);
    }
    output
}

// ---- New 12-sector computational transforms ------------------------------

fn apply_sector_symbols(id: u8, input: &[u16]) -> Vec<u16> {
    let mut output = input.to_vec();
    let direction = (id - 8) as usize;
    for chunk in output.chunks_mut(12) {
        if !chunk.is_empty() {
            let shift = direction % chunk.len();
            chunk.rotate_left(shift);
        }
    }
    output
}

fn invert_sector_symbols(id: u8, input: &[u16]) -> Vec<u16> {
    let mut output = input.to_vec();
    let direction = (id - 8) as usize;
    for chunk in output.chunks_mut(12) {
        if !chunk.is_empty() {
            let shift = direction % chunk.len();
            chunk.rotate_right(shift);
        }
    }
    output
}

// ---- New PI-20 / icosahedron-face computational transforms ---------------

/// Canonical icosahedron topology: 12 vertex IDs and 20 triangular faces.
/// Geometry is not needed; shared vertex pairs define face adjacency exactly.
pub const ICOSAHEDRON_FACES: [[u8; 3]; 20] = [
    [0, 11, 5], [0, 5, 1], [0, 1, 7], [0, 7, 10], [0, 10, 11],
    [1, 5, 9], [5, 11, 4], [11, 10, 2], [10, 7, 6], [7, 1, 8],
    [3, 9, 4], [3, 4, 2], [3, 2, 6], [3, 6, 8], [3, 8, 9],
    [4, 9, 5], [2, 4, 11], [6, 2, 10], [8, 6, 7], [9, 8, 1],
];

fn faces_share_edge(left: &[u8; 3], right: &[u8; 3]) -> bool {
    left.iter().filter(|vertex| right.contains(vertex)).count() == 2
}

fn face_adjacency() -> [[bool; 20]; 20] {
    let mut adjacency = [[false; 20]; 20];
    for left in 0..20 {
        for right in (left + 1)..20 {
            if faces_share_edge(&ICOSAHEDRON_FACES[left], &ICOSAHEDRON_FACES[right]) {
                adjacency[left][right] = true;
                adjacency[right][left] = true;
            }
        }
    }
    adjacency
}

/// Sort all faces by graph distance from `focus`, breaking ties by face ID.
fn face_bfs_order(focus: usize) -> [usize; 20] {
    debug_assert!(focus < 20);
    let adjacency = face_adjacency();
    let mut distances = [u8::MAX; 20];
    let mut queue = [0_usize; 20];
    let (mut head, mut tail) = (0_usize, 0_usize);
    distances[focus] = 0;
    queue[tail] = focus;
    tail += 1;
    while head < tail {
        let face = queue[head];
        head += 1;
        for neighbor in 0..20 {
            if adjacency[face][neighbor] && distances[neighbor] == u8::MAX {
                distances[neighbor] = distances[face] + 1;
                queue[tail] = neighbor;
                tail += 1;
            }
        }
    }
    debug_assert_eq!(tail, 20);
    let mut order = std::array::from_fn::<usize, 20, _>(|index| index);
    order.sort_by_key(|face| (distances[*face], *face));
    order
}

/// For each <=20-symbol chunk, restrict the canonical face order to local
/// indices that exist, then gather those symbols.
fn apply_lens_symbols(id: u8, input: &[u16]) -> Vec<u16> {
    let focus = (id - 20) as usize;
    let order = face_bfs_order(focus);
    let mut output = Vec::with_capacity(input.len());
    for chunk in input.chunks(20) {
        for local_index in order.iter().copied().filter(|index| *index < chunk.len()) {
            output.push(chunk[local_index]);
        }
    }
    output
}

/// Explicit local inverse: scatter each <=20-symbol chunk by the same
/// restricted BFS-distance+face-index order.
fn invert_lens_symbols(id: u8, input: &[u16]) -> Vec<u16> {
    let focus = (id - 20) as usize;
    let order = face_bfs_order(focus);
    let mut output = Vec::with_capacity(input.len());
    for chunk in input.chunks(20) {
        let mut restored = vec![0_u16; chunk.len()];
        let restricted = order.iter().copied().filter(|index| *index < chunk.len());
        for (stream_position, local_index) in restricted.enumerate() {
            restored[local_index] = chunk[stream_position];
        }
        output.extend_from_slice(&restored);
    }
    output
}

