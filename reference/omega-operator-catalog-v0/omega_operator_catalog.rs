#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum Family {
    RnqC2xC2xC2,
    SectorCyclic,
    RadialLens,
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum Provenance {
    /// Inherited operator algebra; exact existing realization at 8 bits.
    InheritedExact,
    /// A new, explicitly specified computational transform.
    NewComputational,
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct OperatorSpec {
    pub id: u8,
    pub name: &'static str,
    pub family: Family,
    pub provenance: Provenance,
    pub inverse_id: u8,
}

macro_rules! op {
    ($id:expr, $name:expr, $family:ident, $provenance:ident, $inverse:expr) => {
        OperatorSpec {
            id: $id,
            name: $name,
            family: Family::$family,
            provenance: Provenance::$provenance,
            inverse_id: $inverse,
        }
    };
}

